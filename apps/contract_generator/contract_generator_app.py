def set_document_font(doc, font_name="Times New Roman", font_size=12):
    """–£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ—Ç —à—Ä–∏—Ñ—Ç –¥–ª—è –≤—Å–µ–≥–æ –¥–æ–∫—É–º–µ–Ω—Ç–∞"""
    # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —à—Ä–∏—Ñ—Ç –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é –¥–ª—è –¥–æ–∫—É–º–µ–Ω—Ç–∞
    style = doc.styles['Normal']
    font = style.font
    font.name = font_name
    font.size = Inches(font_size/72)  # –ö–æ–Ω–≤–µ—Ä—Ç–∏—Ä—É–µ–º –ø—É–Ω–∫—Ç—ã –≤ –¥—é–π–º—ã
    font.color.rgb = RGBColor(0, 0, 0)  # –ß–µ—Ä–Ω—ã–π —Ü–≤–µ—Ç
    
    # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —à—Ä–∏—Ñ—Ç –¥–ª—è –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤
    for heading_level in range(1, 10):
        try:
            heading_style = doc.styles[f'Heading {heading_level}']
            heading_font = heading_style.font
            heading_font.name = font_name
            heading_font.size = Inches(font_size/72)
            heading_font.color.rgb = RGBColor(0, 0, 0)  # –ß–µ—Ä–Ω—ã–π —Ü–≤–µ—Ç
        except:
            pass

def apply_font_to_paragraph(paragraph, font_name="Times New Roman", font_size=12, bold=False):
    """–ü—Ä–∏–º–µ–Ω—è–µ—Ç —à—Ä–∏—Ñ—Ç –∫ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–º—É –ø–∞—Ä–∞–≥—Ä–∞—Ñ—É"""
    for run in paragraph.runs:
        run.font.name = font_name
        run.font.size = Inches(font_size/72)
        run.font.color.rgb = RGBColor(0, 0, 0)  # –ß–µ—Ä–Ω—ã–π —Ü–≤–µ—Ç
        run.font.bold = bold

def apply_font_to_table(table, font_name="Times New Roman", font_size=12):
    """–ü—Ä–∏–º–µ–Ω—è–µ—Ç —à—Ä–∏—Ñ—Ç –∫–æ –≤—Å–µ–º —è—á–µ–π–∫–∞–º —Ç–∞–±–ª–∏—Ü—ã"""
    for row in table.rows:
        for cell in row.cells:
            for paragraph in cell.paragraphs:
                apply_font_to_paragraph(paragraph, font_name, font_size)

